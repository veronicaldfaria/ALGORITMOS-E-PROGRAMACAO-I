# Escreva um programa em Python que permite ao usuário digitar dois números inteiros
# e exibir o resultado para cada uma das seguintes operações, nesta ordem: soma,
# subtração, multiplicação, divisão, divisão truncada, resto e exponenciação. 

n1= int(input())
n2= int(input())
soma = n1 + n2
print(soma)
subtracao = n1 - n2
print(subtracao)
multiplicacao = n1 * n2
print(multiplicacao)
divisao = n1 / n2
print(divisao)
divisaotruncada = n1 // n2
print(divisaotruncada)
modulo = n1 % n2
print(modulo)
exponenciacao = n1 ** n2
print(exponenciacao)